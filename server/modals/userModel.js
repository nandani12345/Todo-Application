import mongoose from "mongoose";
const userSchema = new mongoose.Schema({
  fname: {
    type: String,
    required: true,
  },
  lname: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    unique: true,
    required: true,
  },
  password: {
    type: String,
    unique:true,
    required: true,
  }
});
export default mongoose.model("User", userSchema);
